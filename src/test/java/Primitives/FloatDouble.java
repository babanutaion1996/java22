package Primitives;

public class FloatDouble {

    public static void main(String[] args) {

      //Float and Double Type
      // Both of this data types use for storing decimals numbers
      // Double is a bigger data type, and double type is used by default in Java
      // We have to explicitly tell java to not worry that this is a float
        float avg = 12;
        System.out.println(avg);

         double pi = 3.14159;
        System.out.println(pi);

        double studs = 10;
        System.out.println(studs);//10.0

        float windwows = 12;
        System.out.println(windwows);//12.0
        // By default,  java assigns double type for every decimal number,
        // so it is wise for us to usde double data type for each decimal number

    }




















}
